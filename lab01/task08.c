#include <stdio.h>

int main() {
    int x = 2147483647;

    printf("x     = %d\n", x);
    printf("x + 1 = %d\n", x + 1);

    /*
     * Объяснение:
     * int хранится в 32 битах. Максимальное значение — 2147483647 (0x7FFFFFFF).
     * При прибавлении 1 происходит переполнение: бит знака устанавливается в 1,
     * и число интерпретируется как наименьшее отрицательное — (-2147483648).
     * Это называется целочисленным переполнением (integer overflow).
     */

    return 0;
}
